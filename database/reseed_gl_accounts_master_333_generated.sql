    company_id,
    account_code,
    account_name,
    account_type,
    normal_balance,
    parent_account_id,
    hierarchy_level,
    is_posting,
    is_active,
    sort_order,
    report_group,
    requires_department,
    requires_project,
    requires_cost_center,
    created_by,
    updated_by,
    created_at,
    updated_at
)
SELECT c.id,
       s.account_code,
       s.account_name,
       s.account_type,
       s.normal_balance,
       p.id,
       s.hierarchy_level,
       s.is_posting,
       TRUE,
       s.sort_order,
       s.report_group,
       FALSE,
       FALSE,
       s.requires_cost_center,
       'SEED',
       'SEED',
       NOW(),
       NOW()
FROM org_companies c
JOIN tmp_master_akun_seed s ON TRUE
LEFT JOIN gl_accounts p
  ON p.company_id = c.id
 AND p.account_code = s.parent_code
WHERE c.is_active = TRUE
ON CONFLICT (company_id, account_code) DO UPDATE
SET account_name = EXCLUDED.account_name,
    account_type = EXCLUDED.account_type,
    normal_balance = EXCLUDED.normal_balance,
    parent_account_id = EXCLUDED.parent_account_id,
    hierarchy_level = EXCLUDED.hierarchy_level,
    is_posting = EXCLUDED.is_posting,
    is_active = TRUE,
    sort_order = EXCLUDED.sort_order,
    report_group = EXCLUDED.report_group,
    requires_department = FALSE,
    requires_project = FALSE,
    requires_cost_center = EXCLUDED.requires_cost_center,
    updated_by = 'SEED',
    updated_at = NOW();

DO 
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'public'
          AND table_name = 'gl_accounts'
          AND column_name = 'account_level'
    ) THEN
        UPDATE gl_accounts a
        SET account_level = s.hierarchy_level
        FROM tmp_master_akun_seed s
        WHERE a.account_code = s.account_code
          AND a.account_level IS DISTINCT FROM s.hierarchy_level;
    END IF;
END
